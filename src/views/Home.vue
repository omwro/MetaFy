    <v-container v-if="isLoggedIn()">
        <v-row>
            <v-col>
                <h1 class="text-center">Let's vibe with Metafy!</h1>
            </v-col>
        </v-row>
        <v-row>
            <v-col>
                <h2 class="text-center">Your dynamic playlists</h2>
            </v-col>
        </v-row>
        <v-row>
            <v-col class="playlist-container">
                <v-chip
                    v-for="pl in playlists.dynamics"
                    :key="pl.id"
                    class="playlist-chip"
                >
                    {{ pl.metafy.tagName }}
                </v-chip>
            </v-col>
        </v-row>
        <v-row>
            <v-col>
                <h2 class="text-center">Your tagged playlists</h2>
            </v-col>
        </v-row>
        <v-row>
            <v-col
                v-for="plwt in playlists.tags"
                :key="plwt.categoryName"
            >
                <v-row>
                    <v-col>
                        <h3 class="text-center">{{ plwt.categoryName }}</h3>
                    </v-col>
                </v-row>
                <v-row>
                    <v-col

                        class="playlist-container"
                    >
                        <v-chip
                            v-for="pl in plwt.playlists"
                            :key="pl.id"
                            class="playlist-chip"
                        >
                            {{ pl.metafy.tagName }}
                        </v-chip>
                    </v-col>
                </v-row>
            </v-col>
        </v-row>
        <v-row>
            <v-col>
                <h2 class="text-center">Your untagged playlists</h2>
            </v-col>
        </v-row>
        <v-row>
            <v-col class="playlist-container">
                <v-chip
                    v-for="pl in playlists.others"
                    :key="pl.id"
                    class="playlist-chip"
                >
                    {{ pl.name }}
                </v-chip>
            </v-col>
        </v-row>
    </v-container>
    <v-container v-else>
        <h1>Welcome to Metafy!</h1>
    </v-container>
</template>

<script>
import {SpotifyService} from "@/services/spotifyService.js";
import store from "@/store/store";

export default {
    name: 'Home',
    data: () => ({
        playlists: [],
    }),
    created() {
        SpotifyService.fetchPlaylists().then((result) => {
            this.playlists = SpotifyService.convertPlaylist(result);
        });
    },
    methods: {
        isLoggedIn() {
            return store.getters.isLoggedIn;
        }
    }
}
</script>

